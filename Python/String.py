# Slice string

a = "   I am a computer programmer   "

print(a[0:5])
print(a[:5])
print(a[5:])


# Modify string

print(a.upper())
print(a.lower())
print(a.strip())
print(a.replace('I' ,'H'))
print(a.split())

# Format string

b = f"My age is {10}"
print(b)

# Escape Character

c = "I am a \'programmer\'"
print(c)

print("I am a python \\programmer")
print("I am a python \nprogrammer")
print("I am a python \\programmer")